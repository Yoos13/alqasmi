package yoo13;

public class Mark {
		private String type;
		private Integer Grade;
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public Integer getGrade() {
			return Grade;
		}
		public void setGrade(Integer grade) {
			Grade = grade;
		}
		
}
